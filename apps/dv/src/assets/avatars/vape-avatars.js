export const getAvatarById = (id) => {
  return vapeAvatars.find((avatar) => avatar.id === id) || vapeAvatars[0];
};

export const getRandomAvatar = () => {
  const randomIndex = Math.floor(Math.random() * vapeAvatars.length);
  return vapeAvatars[randomIndex];
};
